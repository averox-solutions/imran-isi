#!/bin/bash -e

escape_quotes() {
    cat <<EOF | sed -e "s/'/\\\\'/g"
$1
EOF
}

avx_new_properties() {
	#
	# Setup averox.properties for avx-web
	#
 sed -i "s/averox.web.serverURL=http:\/\/.*/averox.web.serverURL=http:\/\/$IP/g" \
         $SERVLET_DIR/WEB-INF/classes/averox.properties

 sed -i "s/screenshareRtmpServer=.*/screenshareRtmpServer=$IP/g" \
         $SERVLET_DIR/WEB-INF/classes/averox.properties
}

avx_config() {
	avx_new_properties
	
  #
  # Now update the API examples
	#
	SECRET=$(openssl rand -base64 32 | sed 's/=//g' | sed 's/+//g' | sed 's/\///g')

	HOST=$(cat $SERVLET_DIR/WEB-INF/classes/averox.properties | sed -n '/^averox.web.serverURL/{s/.*\///;p}')

  sed -i "s/securitySalt=.*/securitySalt=$SECRET/g" \
     $SERVLET_DIR/WEB-INF/classes/averox.properties

	#
	# XXX
	if id meteor > /dev/null 2>&1 ; then
		if [ -d /var/log/averox/html5 ]; then
			chown -R meteor:meteor /var/log/averox/html5
		fi
	fi

	#
	# Update the placementsThreshold and imageTagThreshold
	sed -i 's/placementsThreshold=8000/placementsThreshold=800/g' $SERVLET_DIR/WEB-INF/classes/averox.properties
	sed -i 's/imageTagThreshold=8000/imageTagThreshold=800/g'     $SERVLET_DIR/WEB-INF/classes/averox.properties

	#
	# Fix links in welcome text
	sed -i 's#<a href="event:http://www.averox.org/html5">#<a href="https://www.averox.org/html5" target="_blank">#g' \
		$SERVLET_DIR/WEB-INF/classes/averox.properties

	sed -i 's#<a href="http://docs.averox.org/" target="_blank">#<a href="https://docs.averox.org/" target="_blank">#g' \
		$SERVLET_DIR/WEB-INF/classes/averox.properties

  if [ ! -L /usr/share/averox/nginx/web.nginx ]; then
    ln -s /usr/share/averox/nginx/web /usr/share/averox/nginx/web.nginx
  fi

  if [[ ! -L /usr/share/avx-web/logs && -d /usr/share/avx-web/logs ]]; then  # remove old directory (if exists)
    rm -rf /usr/share/avx-web/logs
  fi

  if [ ! -L /usr/share/avx-web/logs ]; then  # create symbolic link
    ln -s /var/log/averox /usr/share/avx-web/logs
  fi
  chown averox:averox /var/log/averox
  touch /var/log/averox/avx-web.log
  chown averox:averox /var/log/averox/avx-web.log

  update-java-alternatives -s java-1.17.0-openjdk-amd64

  # Restart avx-web to deploy new 
  startService avx-web.service || echo "avx-web.service could not be registered or started"
}

case "$1" in
  configure|upgrade|1|2)
  	avx_config
    ;;

    abort-upgrade|abort-remove|abort-deconfigure)

    ;;

    *)
      echo "## postinst called with unknown argument \`$1'" >&2
    ;;
esac

# dh_installdeb will replace this with shell code automatically
# generated by other debhelper scripts.

exit 0


